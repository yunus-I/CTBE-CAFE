export type Locale = "en" | "am";
export type TranslationKey = keyof typeof translations.en;

export function getTranslation(locale: Locale, key: TranslationKey): string {
  return translations[locale]?.[key] ?? translations.en[key] ?? String(key);
}

export function getTranslationOrSelf(locale: Locale, text: string): string {
  if (!text) return "";
  const key = Object.keys(translations.en).find(
    (k) => translations.en[k as TranslationKey] === text
  ) as TranslationKey | undefined;

  if (key) {
    return translations[locale][key];
  }
  return text;
}
